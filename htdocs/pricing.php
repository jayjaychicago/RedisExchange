          <dt class="no-monthly-fees"><strong>Fees only if you make money</strong></dt>
          <dd>Instabid only charges the seller the lowest of $4 or 4% when a bid is successful. No charge to the buyer. Example: One completed transaction bringing the seller $200 will cost $4. One completed transaction bringing the seller $5 will cost 20c. The first 100 transactions are free!</dd>

          <dt class="refunds"><strong>No unsuccesful bid costs</strong></dt>

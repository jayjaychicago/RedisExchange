        <h2>Simple pricing</h2>
        <p>
          Instabid charges the lowest of 4% or $4 per successful charge. This works by accounting for the release of the details of the successful bids to you enabling you to then complete the transaction.
        </p>
        <p>
          <strong>Processing high volume of bids (>10,000 per day per product)?</strong> You can throttle the max amount of bids or pay an extra $0.01 per bids after 10,000 per day per product.
        </p>
        <p>
          Earnings are transferred to your bank account on a 2-day rolling basis.
        </p>
